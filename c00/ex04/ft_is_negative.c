#include <unistd.h>

void	ft_is_negative(int n)
{
	char	e;
	char	p;

	e = 'N';
	p = 'P';
	if (n <= -1)
	{
		write(1, &e, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}

